    private boolean verificarPai(int valor){
        boolean achou = false;
        for(int i = 0; i < 7; i++) {
            if(label[i].getText() != ""){
                if(Integer.parseInt(label[i].getText()) == valor)
                    achou = true;
            }
        }
        if(!achou)
            System.out.println("O Valor pai não consta na arvore");
        
        return achou;
    }
    
    private boolean verificarFilho(int valor){
        boolean achou = false;
         for(int i = 0; i < 7; i++) {
            if(label[i].getText() != ""){
                if(Integer.parseInt(label[i].getText()) == valor){
                    System.out.println("Valor filho já consta na arvore");
                    achou = true;
                }
            }
        }
         return achou;
    }
    
