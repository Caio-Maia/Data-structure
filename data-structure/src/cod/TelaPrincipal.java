    private void atualiza(){
        setVisibleRaiz();
        setVisible();
        
        for(int i = 0; i < 7; i++){
            if(label[i].getText() != null)
                
                label[i].setForeground(Color.black);
        }
    }
    
    private void bInsereRaizActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bInsereRaizActionPerformed
        int valor;
        if(vRaiz.getText() == null){
            System.out.println("Nenhum valor digitado no campo");
        }
        else{
            if(vRaiz.getText() != null){
                valor = Integer.parseInt(vRaiz.getText());
                arv.insereRaiz(valor);
            }
        }
        atualiza();
    }//GEN-LAST:event_bInsereRaizActionPerformed

    private void bInsereEsqActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bInsereEsqActionPerformed

        int vfilho = Integer.parseInt(vFilho.getText());
        int vpai = Integer.parseInt(vPai.getText());
        boolean filho, pai;
        
        filho = verificarFilho(vfilho);
        pai = verificarPai(vpai);
        
        if(filho != true && pai != false)
            arv.insereEsq(vpai, vfilho);
        atualiza();
    }//GEN-LAST:event_bInsereEsqActionPerformed

